    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here
        int c=0;
        String Movie=movie_name.getText();
        String Location=(String)loc.getSelectedItem();
        String Language=(String)language.getSelectedItem();
        String Venues=(String)Venue.getSelectedItem();
        String Type=(String)type.getSelectedItem();
        String Rating=(String)ratings.getSelectedItem();
        try {                                                                    
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/Bookmyshow","root","");
            Statement stmt=con.createStatement();
            String query="SELECT * FROM MOVIES WHERE Movie_Name='"+Movie+"';"; 
            ResultSet rs=stmt.executeQuery(query);
            while(rs.next()) {
                String mov=rs.getString("Movie_Name");
                if(mov.equals(Movie)){
                    c=1;
                }
            }
            if(c!=1) {
                String query1="INSERT INTO MOVIES VALUES('"+Movie+"','"+Type+"','"+Language+"',"+Rating+");";
                stmt.executeUpdate(query1);
            }
            String query2="INSERT INTO MOVIE_DATABASE VALUES('"+Movie+"','"+Venues+"','"+Location+"','',null,null,null);";
            stmt.executeUpdate(query2);
            stmt.close();
            con.close();
            JOptionPane.showMessageDialog(null,"Added successfully!");
            movie_name.setText("");
            ratings.setSelectedIndex(0);
            loc.setSelectedIndex(0);
            type.setSelectedIndex(0);
        }
        catch(ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

