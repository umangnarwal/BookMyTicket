    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here
        DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();            
        String searched_movie=movie_search.getText();
        Options obj=new Options();
        obj.resetor();
        String mtype=obj.get_type();
        //Loading database
        String loc="";
        try{                                                                    
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/Bookmyshow","root","");
            Statement stmt=con.createStatement();
            String query1="SELECT * FROM USERLOCATION WHERE Id=1;";
            ResultSet rs1=stmt.executeQuery(query1);
            while(rs1.next()) {
                loc=rs1.getString("Location");
            }
            String query="SELECT DISTINCT(MOVIES.Movie_Name),MOVIES.Type,Movies.Language,Movies.Ratings,MOVIE_DATABASE.Location FROM MOVIES,MOVIE_DATABASE WHERE MOVIE_DATABASE.Location='"+loc+"' AND MOVIES.Movie_Name Like '"+searched_movie+"%' AND MOVIES.Type Like '"+mtype+"';";
            ResultSet rs=stmt.executeQuery(query);                              
            while(rs.next()) {
                String movie=rs.getString("Movie_Name");
                String type=rs.getString("Type");   
                String language=rs.getString("Language");
                String rating=rs.getString("Ratings"); 
                dtm.addRow(new Object[]{movie,type,language,rating}); 
            }
            rs.close();                                                         
            stmt.close();
            con.close();
        }
        catch(ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

