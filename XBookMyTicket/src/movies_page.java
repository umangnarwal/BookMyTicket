        void resetor()                                                          // function resets the entire table
        {
            DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
            int rows=dtm.getRowCount();
            if(rows>0)
            {
                for(int i=0;i<rows;i++)
                {
                    dtm.removeRow(0);
                }
            }
        }
